#include<stdio.h>

void main() {                         // Void - Void also a Datatype and its size is 0/1
 	                             
	                              // Datatype    Size       Format Specifier
	int age = 45;                 // Integer   - 4 Bytes   - %d
	float price = 67.89;          // Float     - 4 Bytes   - %f
	double salary = 200.837469;   // Double    - 8 Bytes   - %lf
	char grade = 'A';             // Character - 1 Bytes   - %c
	
       printf("Age : %d\n",age);	       //45
       printf("Price : %f\n",price);    //67.889999
       printf("Salary : %lf\n",salary); //200.837469	
       printf("Grade : %c\n",grade);    //A		
}	
